fun main() {
    // Declare principal, rate, and time
    val principal = 1000.0  // Example principal amount
    val rate = 5.0  // Interest rate in percentage
    val time = 3.0  // Time in years

    // Calculate Simple Interest
    val simpleInterest = (principal * rate * time) / 100

    // Output the result
    println("The Simple Interest is: $simpleInterest")
}
