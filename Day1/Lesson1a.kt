fun main() {
    //Using val to declare/define and initialize variables.
    val number1 = 5       // Int
    val number2 = 5.99    // Double
    val letter = 'D'      // Char
    val paid = true       // Boolean 
    val name = "Hello"    // String

    print(number1)
    print(number2)
    print(letter)
    print(paid)
    print(name)
}